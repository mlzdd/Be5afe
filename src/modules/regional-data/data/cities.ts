import type { City, CityId, CountryId } from '../types';

function cityId(iso2: string, name: string): CityId {
  return `${iso2.toLowerCase()}-${name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')}` as CityId;
}

function countryId(iso2: string): CountryId {
  return iso2.toLowerCase() as CountryId;
}

